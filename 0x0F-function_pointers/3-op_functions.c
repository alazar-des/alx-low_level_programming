/**
 * op_sub - return of addition of two numbers
 * @a: first number
 * @b: second number
 *
 * Return: a plus b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - return of subtraction of two numbers
 * @a: first number
 * @b: second number
 *
 * Return: a minus b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - return multiplication of two numbers
 * @a: first number
 * @b: second number
 *
 * Return: a times b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - return divison result
 * @a: numberator
 * @b: denominator
 *
 * Return: a divided by b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - return modulo
 * @a: numberator
 * @b: denominator
 *
 * Return: a modulo b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
